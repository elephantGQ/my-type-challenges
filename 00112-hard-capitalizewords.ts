type CapitalizeWords<
  T extends string,
  F extends boolean = true,
  Res extends string = ''
> = T extends `${infer L}${infer R}`
  ? CapitalizeWords<
      R,
      L extends Alphabet ? false : true,
      F extends true ? `${Res}${Capitalize<L>}` : `${Res}${L}`
    >
  : Res;


